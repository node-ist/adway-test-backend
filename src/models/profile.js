import mongoose, { Schema } from 'mongoose'

const schema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
})

export const Profile = mongoose.model('Profile', schema)
